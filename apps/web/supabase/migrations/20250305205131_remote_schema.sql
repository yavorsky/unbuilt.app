CREATE OR REPLACE FUNCTION "public"."search_by_tech_stack"("techs" "jsonb", "min_confidence" double precision DEFAULT 0.0) RETURNS TABLE("url" "text", "analysis_id" "uuid", "analyzed_at" timestamp with time zone, "confidence_scores" "jsonb")
    LANGUAGE "plpgsql"
    AS $$
declare
  tech_key text;
  tech_value text;
  query_parts text[] := array[]::text[];
  select_parts text;
begin
  -- Build the where clause parts
  for tech_key, tech_value in select * from jsonb_each_text(techs)
  loop
    query_parts := array_append(
      query_parts, 
      format('%I = %L::' || tech_key || '_type and %I_confidence >= %s', 
        tech_key, tech_value, tech_key, min_confidence::text)
    );
  end loop;

  -- Build the dynamic select part for confidence scores
  select string_agg(format('%L, %I_confidence', key, key), ', ')
  from jsonb_object_keys(techs) as t(key)
  into select_parts;
    
  return query execute format(
    'select url, id as analysis_id, analyzed_at, 
     jsonb_build_object(%s) as confidence_scores
     from tech_stack_analysis 
     where %s
     order by analyzed_at desc',
    select_parts,
    array_to_string(query_parts, ' and ')
  );
end;
$$;


