CREATE OR REPLACE FUNCTION "public"."get_unique_technology_websites"("technology_column" "text", "technology_value" "text", "confidence_threshold" double precision, "search_term" "text", "page_number" integer, "items_per_page" integer) RETURNS TABLE("id" "uuid", "url" "text", "analyzed_at" timestamp with time zone, "confidence" double precision, "total_count" bigint)
    LANGUAGE "plpgsql"
    AS $_$
declare
  query text;
begin
  query := format(
    'with ranked_records as (
      select 
        ta.id,
        ta.url,
        ta.analyzed_at,
        ta.%I_confidence as confidence,
        row_number() over (partition by ta.url order by ta.analyzed_at desc) as rn
      from tech_stack_analysis ta
      where 
        ta.%I::text = $1::text
        and ta.%I_confidence >= $2
        and ($3 is null or ta.url ilike ''%%'' || $3 || ''%%'')
    ),
    filtered_records as (
      select * from ranked_records where rn = 1
    )
    select 
      fr.id,
      fr.url,
      fr.analyzed_at,
      fr.confidence,
      (select count(*) from filtered_records) as total_count
    from filtered_records fr
    order by fr.analyzed_at desc
    limit $4
    offset $5',
    technology_column,
    technology_column,
    technology_column
  );

  return query execute query
  using 
    technology_value,
    confidence_threshold,
    search_term,
    items_per_page,
    (page_number - 1) * items_per_page;
end;
$_$;


