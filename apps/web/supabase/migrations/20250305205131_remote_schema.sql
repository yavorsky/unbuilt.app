CREATE OR REPLACE FUNCTION "public"."get_urls_by_technology"("tech_type" "text", "tech_name" "text", "min_confidence" double precision DEFAULT 0.0) RETURNS TABLE("url" "text", "confidence" double precision, "analysis_id" "uuid", "analyzed_at" timestamp with time zone)
    LANGUAGE "plpgsql"
    AS $_$
begin
  return query execute format(
    'select url, %I_confidence as confidence, id, analyzed_at 
     from tech_stack_analysis 
     where %I = %L::' || tech_type || '_type 
     and %I_confidence >= $1
     order by %I_confidence desc',
    tech_type, tech_type, tech_name, tech_type, tech_type
  ) using min_confidence;
end;
$_$;


