CREATE OR REPLACE FUNCTION "public"."get_unique_technologies_count"("confidence_threshold" double precision, "technology_column" "text", "search_term" "text" DEFAULT NULL::"text") RETURNS TABLE("technology_value" "text", "count" bigint)
    LANGUAGE "plpgsql"
    AS $_$
declare
  query text;
begin
  query := format(
    'with ranked_records as (
      select 
        ta.%I::text as tech_value,
        ta.%I_confidence as confidence,
        ta.url,
        row_number() over (partition by ta.url order by ta.analyzed_at desc) as rn
      from tech_stack_analysis ta
      where 
        ta.%I_confidence >= $1
        and ($3 is null or ta.url ilike ''%%'' || $3 || ''%%'')
    ),
    filtered_records as (
      select * from ranked_records where rn = 1
    )
    select 
      fr.tech_value as technology_value,
      count(*) as count
    from filtered_records fr
    group by fr.tech_value
    order by count desc',
    technology_column,
    technology_column,
    technology_column
  );

  return query execute query
  using 
    confidence_threshold,
    technology_column,
    search_term;
end;
$_$;


