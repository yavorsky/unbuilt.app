CREATE OR REPLACE FUNCTION "public"."get_full_analysis_by_id"("analysis_id" "uuid") RETURNS TABLE("analysis" "json", "styling_libs" "json")
    LANGUAGE "plpgsql"
    AS $$
begin
  return query
  select 
    row_to_json(a.*) as analysis,
    coalesce(
      (
        select json_agg(row_to_json(s.*))
        from styling_libraries s
        where s.analysis_id = a.id
      ),
      '[]'::json
    ) as styling_libs
  from tech_stack_analysis a
  where a.id = analysis_id;
end;
$$;


